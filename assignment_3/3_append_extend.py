# append :- it is use to adds single element in the end of the list.
# extend :- it is use to add a list in the end of the another list.

# append example 

a=['neha','nirali','manish']
print(a)
a.append('tejas')
print(a)

# extend example

a=['neha','nirali','manish']
b=['tejas','krina','aarti']
a.extend(b)
print(a)